    private void jcb_albumesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcb_albumesItemStateChanged
        // TODO add your handling code here:

        Album temp = (Album) jcb_albumes.getSelectedItem();
        if (temp != null) {
            System.out.println("j");
            DefaultTableModel modelotabla = (DefaultTableModel) tabla_albumes.getModel();
            modelotabla.getDataVector().removeAllElements();
            for (Canciones c : temp.getLista_canciones()) {
                Object[] newrow = {c.getPadre().getNombre(), c.getNombre(), c.getArtista(), c.getGenero(), c.getDuracion()};
                modelotabla.addRow(newrow);
            }
            tabla_albumes.setModel(modelotabla);
        }

    }//GEN-LAST:event_jcb_albumesItemStateChanged

